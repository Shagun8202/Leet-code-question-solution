class Solution{
public:	
	
	
	int isPalindrome(string s)
	{
	    int n = s.size();
	    int f= 0;
	    int e = n-1;
	    while(f<e){
	        if(s[f] != s[e]){
	            return 0;
	        }
	        f++;
	        e--;
	    }
	    return 1;
	}

};

